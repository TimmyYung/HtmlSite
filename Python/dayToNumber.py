def day_num(x):
    if x == "Sunday":
        print(0)
    elif x == "Monday":
        print(1)
    elif x == "Tuesday":
        print(2)
    elif x == "Wednesday":
        print(3)
    elif x == "Thursday":
        print(4)
    elif x == "Friday":
        print(5)
    elif x == "Saturday":
        print(6)
    else:
        print("None")

day_num("Friday")
day_num("Sunday")