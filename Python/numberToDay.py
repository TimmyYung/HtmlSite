def day_name(x):
    if x == 0:
        print("Sunday")
    elif x == 1:
        print("Monday")
    elif x == 2:
        print("Tuesday")
    elif x == 3:
        print("Wednesday")
    elif x == 4:
        print("Thursday")
    elif x == 5:
        print("Friday")
    elif x == 6:
        print("Saturday")
    else:
        print("None")

day_name(3)
day_name(6)
day_name(42)